public class constructor {
	private int height;
	private String color;
	private int number;

	public constructor(String c, int h, int n) {
		height = h;
		color = c;
		number = n;
	}

	public int getHeight() {
		return height;
	}

	public String getColor() {
		return color;
	}

	public int getNumber() {
		return number;
	}
}
